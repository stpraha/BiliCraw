package com.bilicrawler.fans;

public class Fans {
	private String name;
	private String registeTime;
	private String followNumber;
	private String fansNumber;
	private String mid;
	
	public void setName(String name) {
		this.name = name;
	}
	
	public void setRegisterTime(String time) {
		this.registeTime = time;
	}
	
	public void setFollowNumber(String followNum) {
		this.followNumber = followNum;
	}
	
	public void setFansNumber(String fansNum) {
		this.fansNumber = fansNum;
	}
	
	public void setMid(String mid) {
		this.mid = mid;
	}
	
	public String getName() {
		return name;
	}
	
	public String getRegisterTime() {
		return registeTime;
	}
	
	public String getFollowNumber() {
		return followNumber;
	}
	
	public String getFansNumber() {
		return fansNumber;
	}
	
	public String getMid() {
		return mid;
	}

}
